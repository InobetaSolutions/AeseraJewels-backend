const mongoose = require("mongoose");

const SupportSchema = new mongoose.Schema({
  mobile: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("Support", SupportSchema);
